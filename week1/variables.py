"""
This module demonstrates basic variable usage in Python.
It includes examples of variable assignment, printing, and multiple assignments.
"""

# Variables are used to store values, and they are created when you assign a value to it.
# Example of variables

X = 10           # Integer
Y = 3.14         # Float
NAME = "frodo"   # String
IS_COOL = True   # Boolean

# To print the value of a variable, you can use the print function
print(X)

# You can also assign multiple variables in one line
A, B, C = 1, 2, 3

# You can also assign the same value to multiple variables in one line
D = E = F = 4

# You can also assign a variable to another variable
G = H = 5
G = H
